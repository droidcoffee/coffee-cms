CREATE TABLE IF NOT EXISTS topic(
	id INTEGER PRIMARY KEY AUTO_INCREMENT  PRIMARY KEY,
	title VARCHAR(20),
	content VARCHAR(255),
	addTime BIGINT,
	updateTime BIGINT,
	uid INTEGER,
	uname VARCHAR(20)
)
drop table if EXISTS reply;
CREATE TABLE IF NOT EXISTS reply(
	id INTEGER PRIMARY KEY AUTO_INCREMENT  PRIMARY KEY,
	tid INTEGER,
	content VARCHAR(255),
	addTime BIGINT,
	uid INTEGER,
	uname VARCHAR(20),
	energy INTEGER,
	score INTEGER
)

