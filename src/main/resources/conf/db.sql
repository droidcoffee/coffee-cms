DROP TABLE  users if EXISTS users;
CREATE TABLE IF NOT EXISTS users(
	id INTEGER PRIMARY KEY AUTO_INCREMENT  PRIMARY KEY,
	uname VARCHAR(255),
	pwd VARCHAR(255),
	role INTEGER,
	ADDTIME BIGINT,
	lastUpdateTime BIGINT,
	lastLoginTime BIGINT
)
